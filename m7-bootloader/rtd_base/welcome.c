void welome(char ch, const char bg)
{
	char line[128]; /* must bigger than row number of welcome_chars multiple 8 */
	unsigned int row, col;

	/* from 3 to 14 to stip some blank lines */
	for (col = 3; col < 14; col++) {
		int i = 0;

		ch = 'N';
		for (row = 0; row < sizeof(welcome_chars)/sizeof(welcome_chars[0]); row++) {
			uint8_t b = welcome_chars[row][col];
			uint8_t m;

			if (row == 4)
				ch = 'X';
			else if (row == 7)
				ch = 'P';
			for (m = 0x1; m ; m <<= 1)
				line[i++] = b & m ? ch : bg;
		}
		line[i++] = '\0';
		printf("%c%s%c\n\r", bg, line, bg);
	}
}
